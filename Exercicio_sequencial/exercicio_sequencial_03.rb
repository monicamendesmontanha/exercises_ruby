#Faça um programa que peça dois números e imprima a soma.

print "Digite um número: "
numero_um = gets.to_i

print "Digite outro número: "
numero_dois = gets.to_i

resultado_soma = numero_um + numero_dois

puts "O primeiro número #{numero_um} somado com o segundo número digitado #{numero_dois} é igual a: #{resultado_soma}!"

